//класс для отображения данных о времени прогноза
class TimeClass {
  late String weekday;
  late String day;
  late String month;
  late String year;
  late String hour;
  late String minutes;

  TimeClass({
    required this.weekday,
    required this.day,
    required this.month,
    required this.year,
    required this.hour,
    required this.minutes,
  });
}
